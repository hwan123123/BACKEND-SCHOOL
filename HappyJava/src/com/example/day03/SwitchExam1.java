package com.example.day03;

public class SwitchExam1 {
    public static void main(String[] args) {
        int num = 1;

        switch (num) {
            case 1 :
                System.out.println("1");
                break;
            case 2 :
                System.out.println("2");
                break;
            default:
                System.out.println("??");
        }
    }
}
